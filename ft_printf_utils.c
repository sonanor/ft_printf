#include "ft_printf.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

char	*ft_strchr(const char *str, int ch)
{
	char	*c;

	c = (char *)str;
	while (*c != (char)ch && *c != '\0')
		c++;
	if (*c == (char)ch)
		return (c);
	return (0);
}
